import Foundation

extension UIColor {
    
    class func cDarkBlue() -> UIColor {
        return UIColor(red: 158.0 / 255.0, green: 204.0 / 255.0, blue: 193.0 / 255.0, alpha: 1)
    }
    
    class func cBlue() -> UIColor {
        return UIColor(red: 198.0 / 255.0, green: 229.0 / 255.0, blue: 211.0 / 255.0, alpha: 1)
    }
    
    class func cYellow() -> UIColor {
        return UIColor(red: 234.0 / 255.0, green: 210.0 / 255.0, blue: 76.0 / 255.0, alpha: 1)
    }
    
    class func cGrey() -> UIColor {
        return UIColor(red: 231.0 / 255.0, green: 218.0 / 255.0, blue: 214.0 / 255.0, alpha: 1)
    }
    
    class func cDarkGrey() -> UIColor {
        return UIColor(red: 186.0 / 255.0, green: 286.0 / 255.0, blue: 186.0 / 255.0, alpha: 1)
    }
    
}